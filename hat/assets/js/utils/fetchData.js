import request from './request'
import { deepEqual, doAsyncInterval } from './index'
import { LOAD, LOAD_SUCCESS, LOAD_ERROR } from '../redux/load'
import { DOWNLOAD, DOWNLOAD_SUCCESS, DOWNLOAD_ERROR } from '../redux/download'
import { push } from 'react-router-redux'

export function createUrl (params) {
  let url = '/charts'
  Object.keys(params).forEach((key) => {
    const value = params[key]
    if (value) {
      url += `/${key}/${value}`
    }
  })
  return url
}

export function checkLocation (params, results, dispatch) {
  // Check if we have data for the selected location,
  // if not, redirect
  const selectedLocation = params.location &&
        decodeURIComponent(params.location)
  const validLocation = results.locations.some((location) => {
    return location.ZS === selectedLocation
  })
  if (selectedLocation && !validLocation) {
    // No data for this location, redirect to national
    dispatch(push(createUrl({ ...params, location: '' })))
    return false
  }
  return true
}

export function getFromCookie (name) {
  // https://docs.djangoproject.com/en/dev/ref/csrf/#ajax
  if (document.cookie && document.cookie !== '') {
    let cookies = document.cookie.split(';')
    for (var i = 0; i < cookies.length; i++) {
      let cookie = cookies[i].trim()
      // Does this cookie string begin with the name we want?
      if (cookie.substring(0, name.length + 1) === (name + '=')) {
        return decodeURIComponent(cookie.substring(name.length + 1))
      }
    }
  } else {
    throw new Error('Cannot get cookie')
  }
}

export function fetchTaskResult (createTaskUrl, data, dispatch) {
  // Start a task on the backend, poll it's progress and resolve
  // to a url that has the link to the result.
  dispatch({
    type: DOWNLOAD
  })

  // POST, PUT, DELETE Request need to include the csrftoken generated by django.
  // The token can be found in the cookie.
  let csrftoken = getFromCookie('csrftoken')

  return request([
    ['post', createTaskUrl],
    ['set', 'content-type', 'application/json'],
    ['set', 'X-CSRFToken', csrftoken],
    ['send', data]
  ])
    .then((result) => {
      // The result of create contains the `url` to the task resource.
      // We can use that to check it's status.
      return doAsyncInterval(
        1000,
        // Poll for status
        () => request([
          ['get', result.url],
          ['set', 'accept', 'application/json']
        ]),
        // Check the status
        (result) => result.done
      )
    })
    .then((result) => {
      // When `done` is `true` on the result, it will contain a `result_url` to the data
      dispatch({
        type: DOWNLOAD_SUCCESS,
        payload: result.result_url
      })
    })
    .catch((err) => {
      dispatch({
        type: DOWNLOAD_ERROR,
        payload: err
      })
    })
}

export function fetchUrls (urls, params, oldParams, dispatch, checkResults) {
  if (deepEqual(oldParams, params, true)) {
    return
  }

  dispatch({
    type: LOAD
  })

  const promises = urls.map((config) => {
    return request([
      ['get', config.url],
      ['set', 'accept', 'application/json'],
      ['query', params]
    ])
  })
  return Promise.all(promises)
    .then((results) => {
      // Create a payload object where the key is the name defined for
      // the url and the value is the response content.
      const payload = results.reduce((payload, result, i) => {
        payload[urls[i].name] = result
        return payload
      }, {})
      // Call `checkResults` which can abort and takeover here
      if (checkResults && !checkResults(params, payload, dispatch)) {
        return
      }
      // Collect all responses in one action
      dispatch({
        type: LOAD_SUCCESS,
        payload
      })
    })
    .catch((err) => {
      dispatch({
        type: LOAD_ERROR,
        payload: err
      })
    })
}
